// Helper — builds a compact pep-pedia entry with one pep-pedia study link.
const ppEntry = ({
  id, name, slug, accent, tag, tagBg,
  vialMg, bacMl, doseMcg,
  shortHu, shortEn, shortPl,
  descHu, descEn, descPl,
  type, structure, status, route,
  doseHu, doseEn, dosePl,
  studyTitle, studyAuthors, studyJournal, studyYear, studyPmid, studyUrl,
  findingHu, findingEn, findingPl,
}) => ({
  id, name, image: null,
  accentColor: accent,
  tagColor: tagBg,
  tagText: accent,
  defaultVialMg: vialMg,
  defaultBacMl: bacMl,
  defaultDoseMcg: doseMcg,
  shortDesc: { hu: shortHu, en: shortEn, pl: shortPl },
  description: { hu: descHu, en: descEn, pl: descPl },
  keyInfo: [
    { label: { hu: 'Típus', en: 'Type', pl: 'Typ' }, value: type },
    structure && { label: { hu: 'Szerkezet', en: 'Structure', pl: 'Struktura' }, value: structure },
    { label: { hu: 'Státusz', en: 'Status', pl: 'Status' }, value: status },
    { label: { hu: 'Beadás', en: 'Route', pl: 'Podanie' }, value: route },
    { label: { hu: 'Tárolás', en: 'Storage', pl: 'Przechowywanie' }, value: '2–8°C' },
    { label: { hu: 'Forrás', en: 'Source', pl: 'Źródło' }, value: { hu: 'Placeholder — kép később', en: 'Placeholder — image later', pl: 'Placeholder — obraz później' } },
  ].filter(Boolean),
  dosageInfo: { hu: doseHu, en: doseEn, pl: dosePl },
  studies: [
    studyTitle && {
      tag: { hu: 'Kutatás', en: 'Research', pl: 'Badania' },
      tagColor: tagBg, tagText: accent,
      title: studyTitle, authors: studyAuthors, journal: studyJournal,
      year: studyYear, pmid: studyPmid || null, url: studyUrl,
      finding: { hu: findingHu, en: findingEn, pl: findingPl },
    },
    {
      tag: { hu: 'Pep-Pedia', en: 'Pep-Pedia', pl: 'Pep-Pedia' },
      tagColor: tagBg, tagText: accent,
      title: `${name} — összefoglaló (Pep-Pedia)`,
      authors: 'Pep-Pedia szerkesztőség',
      journal: 'pep-pedia.org', year: '2025', pmid: null,
      url: `https://pep-pedia.org/peptides/${slug}`,
      finding: {
        hu: `Naprakész szakirodalmi összefoglaló a ${name} hatásmechanizmusairól, dózistartományairól és klinikai/kutatási kontextusáról.`,
        en: `Up-to-date literature summary of ${name} mechanisms, dose ranges and clinical/research context.`,
        pl: `Aktualne podsumowanie literatury o mechanizmach, zakresach dawkowania i kontekście klinicznym/badawczym ${name}.`,
      },
    },
  ].filter(Boolean),
})

